int	ft_isalnum(int c)
{
	unsigned char	d;

	d = (unsigned char)c;
	if (('A' <= d && d <= 'Z') || ('a' <= d && d <= 'z'))
		return (1);
	else if ('0' <= d && d <= '9')
		return (1);
	else
		return (0);
}
